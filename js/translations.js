/* =====================================================
   LANGUAGE ENGINE — robust, no bugs
   ===================================================== */
function applyTranslations(lang) {
  var t = translations[lang];
  if (!t) return;
  try { localStorage.setItem('sureTravelLang', lang); } catch(e) {}

  document.querySelectorAll('[data-translate]').forEach(function(el) {
    var key = el.getAttribute('data-translate');
    if (t[key] === undefined) return;
    var tag = el.tagName;
    if (tag === 'INPUT' || tag === 'TEXTAREA') {
      el.placeholder = t[key];
    } else if (tag === 'OPTION') {
      el.textContent = t[key];
    } else {
      el.textContent = t[key];
    }
  });

  document.querySelectorAll('[data-translate-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-translate-placeholder');
    if (t[key]) el.placeholder = t[key];
  });

  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  document.documentElement.lang = lang === 'ki' ? 'rn' : lang;
}

function initLanguage() {
  var saved = 'en';
  try { saved = localStorage.getItem('sureTravelLang') || 'en'; } catch(e) {}
  if (!translations[saved]) saved = 'en';
  applyTranslations(saved);

  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      applyTranslations(this.getAttribute('data-lang'));
    });
  });
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLanguage);
} else {
  initLanguage();
}
